# Display numbers divisible by 5 from a list
# Exercise 6

# Creating pseudocode
number_list = [10, 20, 33, 46, 55]

# Creating code that will print the numbers that are divisible by five
print("Given list: ", number_list)
print("Numbers that are divisible by five are: ")

# Creating the code that will check if the numbers are divisible by five
for number in number_list:
    if number % 5 == 0:
        # Create a code that will print the number
        print(number)

